public class Mejilla
{
    private String color;
    private int porcentajeGrasa;

    public Mejilla()
    {
        color="chapeada";
        porcentajeGrasa=23;
    }
    public Mejilla(String color, int porcentajeGrasa)
    {
        this.color=color;
        this.porcentajeGrasa=porcentajeGrasa;
    }
}