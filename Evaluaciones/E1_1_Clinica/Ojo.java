public class Ojo
{
    private String color;
    private double aumentoNecesario;

    public Ojo()
    {
        color="Azul";
        aumentoNecesario=2.25;
    }
    public Ojo(String color, double aumentoNecesario)
    {
        this.color=color;
        this.aumentoNecesario=aumentoNecesario;
    }
}